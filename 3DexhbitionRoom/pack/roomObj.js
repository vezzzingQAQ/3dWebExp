function initBasicGround(){
    for(let x=-10;x<10;x++){
        for(let z=-10;z<10;z++){
            objects.objectsList.push(
                {
                    name:"ground",
                    type:1,//1标准长方体
                    paraList:{//携带的参数列表
                        x:x/5,
                        z:z/5,
                    },
                    position:{
                        x:x*100,
                        z:z*100,
                        y:-50,
                    },
                    size:{
                        x:100,
                        z:100,
                        y:100,
                    },
                    strokeWeight:10,
                    stroke:null,
                    fill:[0],
                    change(t){
                        let zv=Math.sin(this.paraList.x*this.paraList.z+t/10);
                        this.stroke=[
                            zv*120+120,
                            120-zv*120,
                            zv*60+60,
                        ];
                        this.strokeWeight=(zv+1)*10;
                    },
                }
            )
        }
    }    
}
