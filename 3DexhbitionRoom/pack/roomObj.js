function initBasicCelling(){
    for(let x=-10;x<10;x++){
        for(let z=-10;z<10;z++){
            let zValue=Math.sin((x/3)*(x/3)+(z/3)*(z/3))+1;
            objects.objectsList.push(
                {
                    name:"sky",
                    type:1,//1标准长方体
                    paraList:{//携带的参数列表
                        x:x/5,
                        z:z/5,
                    },
                    position:{
                        x:x*100,
                        z:z*100,
                        y:1500-zValue*100,
                    },
                    size:{
                        x:100,
                        z:100,
                        y:zValue*200,
                    },
                    strokeWeight:10,
                    stroke:[zValue*60+60,60-zValue*120,100],
                    fill:[0],
                    change(t){
                        this.position.y=Math.sin(this.paraList.x*this.paraList.x+this.paraList.z*this.paraList.z+t/40)*150+1300;
                    },
                }
            )
        }
    }    
}
function initBasicDisplayScreen(name,position,heading,fuc){
    let size,displayCubeSize;
    if(heading==0){
        size={x:300,z:20,y:300};
        displayCubeSize={x:300,z:0,y:300};
    }else{
        size={x:20,z:300,y:300};
        displayCubeSize={x:0,z:300,y:300};
    }
    objects.objectsList.push(
        {
            name:name,
            type:11,//11展示屏
            position:position,
            size:size,
            displayCubeSize:displayCubeSize,
            fuc:fuc,
            stroke:[255,255,0],
            fill:null,
            bump:{
                size:size
            }
        }
    );
}
